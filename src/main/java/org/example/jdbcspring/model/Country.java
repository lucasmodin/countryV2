package org.example.jdbcspring.model;

public class Country {
    private Long id;
    private String name;

    public Country() {

    }

    public Country(Long id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
